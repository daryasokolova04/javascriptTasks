console.log(typeof 'Hello'); //строка

console.log(typeof 10); //число

console.log(typeof {}); //краткая запись создания объекта

console.log(typeof null); //объект (ошибка в языке)

console.log(typeof undefined); //undefined - отдельный тип данных

console.log(typeof true); //булевый тип данный

console.log(typeof []); //масссив - разновидность объекта

console.log(typeof (() => {})); //функция, возвращает пустой объект
